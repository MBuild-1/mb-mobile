class ProductFromMessage {
  String id;
  String userId;
  String productBrandId;
  String name;
  String slug;
  String description;
  String productCategoryId;
  String provinceId;

  ProductFromMessage({
    required this.id,
    required this.userId,
    required this.productBrandId,
    required this.name,
    required this.slug,
    required this.description,
    required this.productCategoryId,
    required this.provinceId
  });
}